    if center is not None:
        vmax = float(np.nanmax(np.abs(mat - center)))
        vmin = center - vmax
        vmax = center + vmax
    else:
        vmin = float(np.nanmin(mat))
        vmax = float(np.nanmax(mat))
    im = ax.imshow(mat, origin="lower", aspect="auto", cmap=cmap, vmin=vmin, vmax=vmax)
    ax.set_title(title)
    ax.set_xlabel("Forward horizon")
    ax.set_ylabel("Absolute OFI_scaled decile")
    ax.set_xticks(np.arange(len(horizons)))
    ax.set_xticklabels([f"{h}s" for h in horizons], rotation=35, ha="right")
    ax.set_yticks(np.arange(len(deciles)))
    ax.set_yticklabels([str(d) for d in deciles])
    cbar = fig.colorbar(im, ax=ax)
    cbar.set_label(value_col)
    fig.tight_layout()
    out = PLOTS / filename
    fig.savefig(out, dpi=160)
    plt.close(fig)
    return out


