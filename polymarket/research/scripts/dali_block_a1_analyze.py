def surface_matrix(surface: pd.DataFrame, value_col: str) -> tuple[np.ndarray, list[int], list[int]]:
    horizons = sorted(surface["horizon_sec"].dropna().astype(int).unique())
    deciles = sorted(surface["decile"].dropna().astype(int).unique())
    mat = (
        surface.pivot(index="decile", columns="horizon_sec", values=value_col)
        .reindex(index=deciles, columns=horizons)
        .to_numpy(dtype=float)
    )
    return mat, horizons, deciles


def plot_surface_heatmap(
    surface: pd.DataFrame,
    value_col: str,
    title: str,
    filename: str,
    cmap: str,
    center: float | None = None,
) -> Path | None:
    if surface.empty:
        return None
    mat, horizons, deciles = surface_matrix(surface, value_col)
