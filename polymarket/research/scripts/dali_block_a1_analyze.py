def fmt_cell(value: object, kind: str = "text") -> str:
    if pd.isna(value):
        return "n/a"
    if kind == "pct":
        return pct(float(value))
    if kind == "bps":
        return bps(float(value))
    if kind == "int":
        return f"{int(value):,}"
    if kind == "float":
        return f"{float(value):.2f}"
    text = str(value)
    return text.replace("|", "/")


def markdown_table(headers: list[str], rows: list[list[object]]) -> str:
    if not rows:
        return "_No rows._"
    header = "| " + " | ".join(headers) + " |"
    sep = "| " + " | ".join(["---"] * len(headers)) + " |"
    body = ["| " + " | ".join(str(cell) for cell in row) + " |" for row in rows]
    return "\n".join([header, sep, *body])


def short_market(row: pd.Series) -> str:
    slug = str(row.get("slug") or "")
