def decile_aggregate(df: pd.DataFrame, results: pd.DataFrame) -> pd.DataFrame:
    depth = (
        results.groupby(["run_id", "market_id"], as_index=False)["mean_depth_at_touch"]
        .first()
        .rename(columns={"mean_depth_at_touch": "market_mean_depth"})
    )
    out_rows: list[dict[str, object]] = []
    base = df.merge(
        depth,
        left_on=["run_id", "market_key"],
        right_on=["run_id", "market_id"],
        how="left",
        suffixes=("", "_depth"),
    )
    for horizon in HORIZONS:
        sig = f"ofi_market_{horizon}s"
        y = f"future_return_bps_{horizon}s"
        sub = base[
            base["is_book_state_complete"].fillna(False)
            & base[sig].notna()
            & base[y].notna()
            & base["market_mean_depth"].gt(0)
        ].copy()
        sub["ofi_scaled"] = sub[sig] / sub["market_mean_depth"]
        sub = sub[np.isfinite(sub["ofi_scaled"]) & sub["ofi_scaled"].ne(0) & np.isfinite(sub[y])]
        if len(sub) < 50:
            continue
        sub["abs_signal"] = sub["ofi_scaled"].abs()
        try:
            sub["decile"] = pd.qcut(sub["abs_signal"], 10, labels=False, duplicates="drop") + 1
        except ValueError:
            continue
        for decile, rows in sub.groupby("decile", sort=True):
            rows = rows.copy()
            hit = np.sign(rows["ofi_scaled"]) == np.sign(rows[y])
            hit = hit[(np.sign(rows["ofi_scaled"]) != 0) & (np.sign(rows[y]) != 0)]
            hit_rate = float(hit.mean()) if len(hit) else math.nan
            lo, hi = block_bootstrap_hit(rows, "ofi_scaled", y, RNG_SEED + horizon * 100 + int(decile))
            out_rows.append({
                "horizon_sec": horizon,
                "decile": int(decile),
                "n": int(len(rows)),
                "mean_abs_ofi_scaled": float(rows["abs_signal"].mean()),
                "mean_next_mid_return_bps": float(rows[y].mean()),
                "directional_return_bps": float((np.sign(rows["ofi_scaled"]) * rows[y]).mean()),
                "hit_rate": hit_rate,
                "hit_rate_ci_lo": lo,
                "hit_rate_ci_hi": hi,
            })
    return pd.DataFrame(out_rows)


def ofi_level(decile: int) -> str:
    if decile <= 3:
        return "low"
    if decile <= 7:
        return "middle"
    if decile <= 9:
        return "high"
    return "extreme"


